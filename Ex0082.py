#Função que adapta ao tamanho da frase

def escreva(fraseParam):
    print("~" * len(fraseParam)); 
    print(fraseParam); 
    print("~" * len(fraseParam)); 
    
    
#Codigo Principal

escreva("Estou!")
escreva("Estou muito!")
escreva("Estou muito bonito!")
escreva("Estou muito bonito hoje!")